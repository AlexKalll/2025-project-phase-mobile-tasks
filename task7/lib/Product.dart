class Product {
  String id;
  String name;
  String description;
  double price;
  String image;
  String size;
  String rating;
  String category;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.size,
    required this.rating,
    required this.category,
  });
}
